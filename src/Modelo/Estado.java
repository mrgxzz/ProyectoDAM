package Modelo;

/**
 *
 * @author Manu Romeo
 */
public class Estado {
    private int idEstado;
    private int estado;

    public Estado() {
    }

    public Estado(int idEstado, int estado) {
        this.idEstado = idEstado;
        this.estado = estado;
    }

    public int getIdEstado() {
        return idEstado;
    }

    public void setIdEstado(int idEstado) {
        this.idEstado = idEstado;
    }

    public int getEstado() {
        return estado;
    }

    public void setEstado(int estado) {
        this.estado = estado;
    }
    
    
}
